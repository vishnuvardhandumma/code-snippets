# For loop - simple range
for i in range(5):   # 0 to 4
    print("For loop:", i)

# For loop with list
fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print("Fruit:", fruit)

# While loop
count = 1
while count <= 5:
    print("While loop:", count)
    count += 1

# Nested loop
for i in range(1, 4):
    for j in range(1, 4):
        print(f"i={i}, j={j}")
